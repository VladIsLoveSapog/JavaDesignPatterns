package pattern3_behavior.behavior4_state.code.example1_order_state;

/**
 * Интерфейс состояния посылки — роль <b>State</b> (Состояние) в паттерне «Состояние» (State).
 *
 * <p>Данный интерфейс определяет контракт для всех конкретных состояний посылки.
 * Каждое конкретное состояние ({@link OrderedState}, {@link DeliveredState}, {@link ReceivedState})
 * реализует этот интерфейс и предоставляет собственную логику переходов и вывода статуса.</p>
 *
 * <p>Все действия, которые зависят от текущего состояния, делегируются контекстом ({@link Package})
 * объекту, реализующему данный интерфейс. Благодаря этому контекст не содержит условной логики
 * (if/switch), а поведение полностью определяется текущим объектом-состоянием.</p>
 *
 * <p>Методы интерфейса:</p>
 * <ul>
 *     <li>{@link #next(Package)} — переход к следующему состоянию в цепочке;</li>
 *     <li>{@link #prev(Package)} — возврат к предыдущему состоянию;</li>
 *     <li>{@link #printStatus()} — вывод информации о текущем статусе посылки.</li>
 * </ul>
 *
 * @see Package
 * @see OrderedState
 * @see DeliveredState
 * @see ReceivedState
 */
public interface PackageState {

    /**
     * Переход посылки в следующее состояние.
     *
     * <p>Каждое конкретное состояние самостоятельно определяет, какое состояние является следующим.
     * Если текущее состояние является конечным (терминальным), переход не выполняется.</p>
     *
     * @param pkg посылка (контекст), которая будет переведена в следующее состояние.
     */
    void next(Package pkg);

    /**
     * Переход посылки в предыдущее состояние.
     *
     * <p>Каждое конкретное состояние самостоятельно определяет, какое состояние является предыдущим.
     * Если текущее состояние является начальным, переход не выполняется.</p>
     *
     * @param pkg посылка (контекст), которая будет переведена в предыдущее состояние.
     */
    void prev(Package pkg);

    /**
     * Вывод сведений о текущем статусе посылки.
     *
     * <p>Каждое конкретное состояние выводит информацию, соответствующую этапу обработки посылки.</p>
     */
    void printStatus();
}
