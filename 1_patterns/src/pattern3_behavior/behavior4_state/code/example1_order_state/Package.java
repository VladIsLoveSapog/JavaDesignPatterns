package pattern3_behavior.behavior4_state.code.example1_order_state;

/**
 * Класс посылки — роль <b>Context</b> (Контекст) в паттерне «Состояние» (State).
 *
 * <p>Контекст хранит ссылку на объект текущего состояния ({@link PackageState}) и делегирует
 * ему всё поведение, зависящее от состояния. Сам контекст не содержит условной логики —
 * он лишь перенаправляет вызовы соответствующему объекту-состоянию.</p>
 *
 * <p>При создании посылки начальным состоянием устанавливается {@link OrderedState} (заказана).
 * Далее состояние может меняться по линейной цепочке:</p>
 * <pre>
 *     OrderedState → DeliveredState → ReceivedState
 * </pre>
 *
 * <p>Смена состояния происходит изнутри самих объектов-состояний через метод {@link #setState(PackageState)},
 * что позволяет инкапсулировать логику переходов в конкретных состояниях, а не в клиентском коде.</p>
 *
 * @see PackageState
 * @see OrderedState
 * @see DeliveredState
 * @see ReceivedState
 */
public class Package {

    /** Ссылка на текущее состояние посылки. По умолчанию — «Заказана» ({@link OrderedState}). */
    private PackageState state = new OrderedState();

    /**
     * Возвращает текущее состояние посылки.
     *
     * @return объект текущего состояния, реализующий {@link PackageState}.
     */
    public PackageState getState() {
        return state;
    }

    /**
     * Устанавливает новое состояние посылки.
     *
     * <p>Метод вызывается конкретными состояниями при выполнении переходов
     * ({@link PackageState#next(Package)}, {@link PackageState#prev(Package)}).</p>
     *
     * @param state новое состояние, в которое переводится посылка.
     */
    public void setState(PackageState state) {
        this.state = state;
    }

    /**
     * Перевод посылки в предыдущее состояние.
     *
     * <p>Делегирует вызов текущему объекту-состоянию. Если текущее состояние является начальным
     * ({@link OrderedState}), переход не произойдёт.</p>
     */
    public void previousState() {
        // Делегируем переход текущему состоянию
        state.prev(this);
    }

    /**
     * Перевод посылки в следующее состояние.
     *
     * <p>Делегирует вызов текущему объекту-состоянию. Если текущее состояние является конечным
     * ({@link ReceivedState}), переход не произойдёт.</p>
     */
    public void nextState() {
        // Делегируем переход текущему состоянию
        state.next(this);
    }

    /**
     * Вывод информации о текущем состоянии посылки.
     *
     * <p>Делегирует вызов текущему объекту-состоянию, который выводит соответствующее сообщение.</p>
     */
    public void printStatus() {
        // Делегируем вывод статуса текущему состоянию
        state.printStatus();
    }
}
