package pattern3_behavior.behavior4_state.code.example2_game;

/**
 * Конкретное состояние (ConcreteState) — игрок стоит на месте.
 *
 * <p>Реализует интерфейс {@link State} и описывает поведение персонажа,
 * когда он находится в состоянии покоя (стоит). Это начальное состояние
 * игрока и состояние, в которое он возвращается после остановки.
 *
 * <p><b>Допустимые переходы из этого состояния:</b>
 * <ul>
 *   <li>{@code pressRun} — игрок начинает бежать → переход в {@link RunningState}</li>
 *   <li>{@code pressJump} — игрок прыгает → переход в {@link JumpingState}</li>
 * </ul>
 *
 * <p><b>Недопустимые действия:</b>
 * <ul>
 *   <li>{@code pressStop} — игрок уже стоит, переход не выполняется</li>
 * </ul>
 *
 * <p><b>Роль в паттерне State:</b><br>
 * Это один из классов <i>ConcreteState</i>. Каждое конкретное состояние
 * инкапсулирует поведение, соответствующее определённому состоянию контекста
 * ({@link Player}), и самостоятельно управляет переходами.
 */
public class StandingState implements State {

    /**
     * Обрабатывает нажатие кнопки «бежать» в состоянии покоя.
     *
     * <p>Игрок начинает бег — допустимый переход из состояния «стоит».
     * Состояние меняется на {@link RunningState}.
     *
     * @param player контекст (игрок), которому устанавливается новое состояние
     */
    @Override
    public void pressRun(Player player) {
        System.out.println("Player starts running.");
        // Переход: стоит → бежит
        player.setState(new RunningState());
    }

    /**
     * Обрабатывает нажатие кнопки «прыгнуть» в состоянии покоя.
     *
     * <p>Игрок прыгает с места — допустимый переход из состояния «стоит».
     * Состояние меняется на {@link JumpingState}.
     *
     * @param player контекст (игрок), которому устанавливается новое состояние
     */
    @Override
    public void pressJump(Player player) {
        System.out.println("Player jumps.");
        // Переход: стоит → прыгает
        player.setState(new JumpingState());
    }

    /**
     * Обрабатывает нажатие кнопки «остановиться» в состоянии покоя.
     *
     * <p>Игрок уже стоит — действие не имеет эффекта, переход не выполняется.
     * Выводится информационное сообщение о том, что игрок уже находится
     * в состоянии покоя.
     *
     * @param player контекст (игрок), состояние которого не изменяется
     */
    @Override
    public void pressStop(Player player) {
        // Игрок уже стоит — переход не требуется
        System.out.println("Player is already standing.");
    }
}
