package pattern3_behavior.behavior4_state.code.example2_game;

/**
 * Конкретное состояние (ConcreteState) — игрок в прыжке.
 *
 * <p>Реализует интерфейс {@link State} и описывает поведение персонажа,
 * когда он находится в воздухе (прыгает). Это наиболее ограниченное
 * состояние: в воздухе нельзя ни бежать, ни прыгнуть повторно —
 * единственное допустимое действие — приземлиться (остановиться).
 *
 * <p><b>Допустимые переходы из этого состояния:</b>
 * <ul>
 *   <li>{@code pressStop} — игрок приземляется → переход в {@link StandingState}</li>
 * </ul>
 *
 * <p><b>Недопустимые действия:</b>
 * <ul>
 *   <li>{@code pressRun} — нельзя бежать в воздухе, переход не выполняется</li>
 *   <li>{@code pressJump} — нельзя прыгнуть повторно, переход не выполняется</li>
 * </ul>
 *
 * <p><b>Роль в паттерне State:</b><br>
 * Это один из классов <i>ConcreteState</i>. Демонстрирует, как паттерн
 * «Состояние» позволяет естественным образом ограничивать доступные действия:
 * вместо проверок {@code if (isJumping)} логика ограничений инкапсулирована
 * в самом классе состояния.
 */
public class JumpingState implements State {

    /**
     * Обрабатывает нажатие кнопки «бежать» в состоянии прыжка.
     *
     * <p>В воздухе нельзя начать бег — действие запрещено.
     * Выводится информационное сообщение, переход не выполняется.
     *
     * @param player контекст (игрок), состояние которого не изменяется
     */
    @Override
    public void pressRun(Player player) {
        // Бег в воздухе невозможен — действие заблокировано
        System.out.println("Player cannot run while jumping.");
    }

    /**
     * Обрабатывает нажатие кнопки «прыгнуть» в состоянии прыжка.
     *
     * <p>Двойной прыжок не поддерживается — действие запрещено.
     * Выводится информационное сообщение, переход не выполняется.
     *
     * @param player контекст (игрок), состояние которого не изменяется
     */
    @Override
    public void pressJump(Player player) {
        // Повторный прыжок в воздухе невозможен
        System.out.println("Player is already jumping.");
    }

    /**
     * Обрабатывает нажатие кнопки «остановиться» в состоянии прыжка.
     *
     * <p>Игрок приземляется и встаёт — единственный допустимый переход
     * из состояния прыжка. Состояние меняется на {@link StandingState}.
     *
     * @param player контекст (игрок), которому устанавливается новое состояние
     */
    @Override
    public void pressStop(Player player) {
        System.out.println("Player lands and stands.");
        // Переход: прыгает → стоит (приземление)
        player.setState(new StandingState());
    }
}
