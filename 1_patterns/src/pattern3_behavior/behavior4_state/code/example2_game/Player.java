package pattern3_behavior.behavior4_state.code.example2_game;

/**
 * Контекст (Context) в паттерне «Состояние» — класс игрового персонажа.
 *
 * <p>Хранит ссылку на текущее состояние ({@link State}) и делегирует
 * ему обработку всех игровых действий: бег, прыжок и остановка.
 * Сам {@code Player} не содержит условной логики (if/switch) для
 * определения поведения — вместо этого каждое состояние самостоятельно
 * решает, как реагировать на действие и в какое состояние перейти.
 *
 * <p><b>Роль в паттерне State:</b><br>
 * Это класс <i>Context</i> — объект, поведение которого меняется
 * в зависимости от внутреннего состояния. Контекст предоставляет метод
 * {@link #setState(State)} для переключения состояний, который вызывается
 * конкретными состояниями при переходах.
 *
 * <p><b>Начальное состояние:</b> при создании игрока устанавливается
 * {@link StandingState} — персонаж стоит на месте и готов к действию.
 *
 * <p><b>Диаграмма переходов:</b><br>
 * StandingState → (бег) → RunningState → (прыжок) → JumpingState → (стоп) → StandingState
 */
public class Player {

    /** Текущее состояние игрока, определяющее его поведение */
    private State currentState;

    /**
     * Создаёт нового игрока с начальным состоянием {@link StandingState}.
     *
     * <p>По умолчанию персонаж находится в состоянии покоя (стоит),
     * что является естественной отправной точкой для игрового процесса.
     */
    public Player() {
        // Начальное состояние — стоя (StandingState)
        currentState = new StandingState();
    }

    /**
     * Устанавливает новое состояние игрока.
     *
     * <p>Вызывается конкретными состояниями ({@link StandingState},
     * {@link RunningState}, {@link JumpingState}) при выполнении
     * перехода из одного состояния в другое.
     *
     * @param state новое состояние, в которое переходит игрок
     */
    public void setState(State state) {
        this.currentState = state;
    }

    /**
     * Обрабатывает нажатие кнопки «бежать».
     *
     * <p>Делегирует обработку текущему состоянию, которое само
     * определяет допустимость действия и возможный переход.
     */
    public void pressRun() {
        // Делегирование действия текущему состоянию
        currentState.pressRun(this);
    }

    /**
     * Обрабатывает нажатие кнопки «прыгнуть».
     *
     * <p>Делегирует обработку текущему состоянию, которое само
     * определяет допустимость действия и возможный переход.
     */
    public void pressJump() {
        // Делегирование действия текущему состоянию
        currentState.pressJump(this);
    }

    /**
     * Обрабатывает нажатие кнопки «остановиться».
     *
     * <p>Делегирует обработку текущему состоянию, которое само
     * определяет допустимость действия и возможный переход.
     */
    public void pressStop() {
        // Делегирование действия текущему состоянию
        currentState.pressStop(this);
    }

    /**
     * Возвращает имя текущего состояния игрока.
     *
     * <p>Используется для отображения и отладки — извлекает
     * простое имя класса текущего состояния через рефлексию.
     *
     * @return простое имя класса текущего состояния (например, {@code "StandingState"})
     */
    public String getStateName() {
        // Получаем имя класса текущего состояния через рефлексию
        return currentState.getClass().getSimpleName();
    }
}
