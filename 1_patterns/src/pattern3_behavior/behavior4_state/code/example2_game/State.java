package pattern3_behavior.behavior4_state.code.example2_game;

/**
 * Интерфейс состояния (State) в паттерне «Состояние» для игровой системы.
 *
 * <p>Определяет контракт для всех конкретных состояний игрока.
 * Каждый метод соответствует действию, которое может выполнить игрок:
 * бежать, прыгать или остановиться. Контекст ({@link Player}) хранит
 * ссылку на текущее состояние и делегирует ему обработку всех действий.
 *
 * <p><b>Роль в паттерне State:</b><br>
 * Это интерфейс <i>State</i> — абстракция, объединяющая все возможные
 * состояния игрока. Конкретные реализации ({@link StandingState},
 * {@link RunningState}, {@link JumpingState}) определяют поведение
 * для каждого действия и управляют переходами между состояниями.
 *
 * <p><b>Когда использовать:</b> когда поведение объекта зависит от его
 * внутреннего состояния и должно меняться динамически во время выполнения,
 * а количество состояний конечно и заранее известно.
 */
public interface State {

    /**
     * Обрабатывает нажатие кнопки «бежать».
     *
     * <p>Каждое конкретное состояние определяет, может ли игрок начать бег
     * из текущего состояния, и при необходимости выполняет переход.
     *
     * @param player контекст (игрок), состояние которого может быть изменено
     */
    void pressRun(Player player);

    /**
     * Обрабатывает нажатие кнопки «прыгнуть».
     *
     * <p>Каждое конкретное состояние определяет, может ли игрок прыгнуть
     * из текущего состояния, и при необходимости выполняет переход.
     *
     * @param player контекст (игрок), состояние которого может быть изменено
     */
    void pressJump(Player player);

    /**
     * Обрабатывает нажатие кнопки «остановиться».
     *
     * <p>Каждое конкретное состояние определяет, может ли игрок остановиться
     * из текущего состояния, и при необходимости выполняет переход.
     *
     * @param player контекст (игрок), состояние которого может быть изменено
     */
    void pressStop(Player player);
}
