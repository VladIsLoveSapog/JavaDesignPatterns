package pattern3_behavior.behavior4_state.code.example3_traffic_light;

/**
 * Светофор — контекст паттерна Состояние (Context).
 *
 * <p>Хранит ссылку на текущий объект-состояние ({@link TrafficLightState})
 * и делегирует ему переключение сигнала. Сам светофор не содержит логики
 * переходов — она инкапсулирована в конкретных состояниях
 * ({@link RedLight}, {@link GreenLight}, {@link YellowLight}).
 *
 * <p><b>Начальное состояние:</b> {@link RedLight} — светофор начинает
 * работу с красного сигнала, как это принято в реальных светофорах
 * при включении.
 *
 * <p><b>Делегирование:</b> метод {@link #nextSignal()} не знает, какой
 * сигнал будет следующим — он просто вызывает
 * {@link TrafficLightState#switchLight(TrafficLight)} у текущего состояния,
 * а состояние само решает, на какой сигнал переключить светофор.
 */
public class TrafficLight {

    /** Текущее состояние светофора. */
    private TrafficLightState state;

    /**
     * Создаёт светофор с начальным состоянием {@link RedLight}.
     */
    public TrafficLight() {
        this.state = new RedLight();    // светофор начинает с красного сигнала
    }

    /**
     * Устанавливает новое состояние светофора.
     *
     * <p>Вызывается конкретными состояниями при переключении сигнала.
     * Клиентский код обычно не вызывает этот метод напрямую —
     * смена состояния происходит через {@link #nextSignal()}.
     *
     * @param state новое состояние светофора
     */
    public void setState(TrafficLightState state) {
        this.state = state;
    }

    /**
     * Переключает светофор на следующий сигнал.
     *
     * <p>Делегирует переключение текущему объекту-состоянию.
     * Контекст не знает, какой сигнал будет следующим —
     * это решает текущее состояние.
     */
    public void nextSignal() {
        state.switchLight(this);    // делегируем переключение текущему состоянию
    }

    /**
     * Возвращает название цвета текущего сигнала.
     *
     * @return цвет текущего сигнала (например, "Красный")
     */
    public String getCurrentColor() {
        return state.getColor();
    }
}
