package pattern3_behavior.behavior8_mediator.code.example3_airport;

/**
 * Интерфейс посредника (Mediator) — диспетчерская башня аэропорта.
 *
 * <p>Определяет контракт для управления воздушным движением: регистрация
 * самолётов, обработка запросов на посадку и взлёт. Все самолёты общаются
 * только через башню, не зная друг о друге.
 *
 * <p><b>Аналогия:</b> в реальном аэропорту пилоты не переговариваются
 * между собой напрямую — все запросы проходят через диспетчерскую башню,
 * которая координирует использование взлётно-посадочной полосы (ВПП).
 *
 * <p><b>Роль в паттерне:</b> Mediator (интерфейс посредника).
 */
public interface ControlTower {

    /**
     * Регистрирует самолёт в системе управления аэропорта.
     *
     * @param aircraft самолёт для регистрации
     */
    void registerAircraft(Aircraft aircraft);

    /**
     * Обрабатывает запрос самолёта на посадку.
     * Башня решает, свободна ли ВПП, и разрешает или отклоняет запрос.
     *
     * @param aircraft самолёт, запрашивающий посадку
     */
    void requestLanding(Aircraft aircraft);

    /**
     * Обрабатывает запрос самолёта на взлёт.
     * После взлёта ВПП освобождается, и башня уведомляет остальные самолёты.
     *
     * @param aircraft самолёт, запрашивающий взлёт
     */
    void requestTakeoff(Aircraft aircraft);
}
