package pattern3_behavior.behavior8_mediator.code.example1_chatroom;

/**
 * Интерфейс посредника (Mediator) для чат-комнаты с адресной доставкой.
 *
 * <p>Определяет контракт, которому должна соответствовать любая реализация
 * чат-комнаты. Благодаря интерфейсу коллеги ({@link User}) зависят от
 * абстракции, а не от конкретного класса — это позволяет подменить
 * реализацию посредника (например, для тестирования или логирования)
 * без изменения кода коллег.
 *
 * <p><b>Роль в паттерне:</b> Mediator (интерфейс посредника).
 */
public interface IChatRoom {

    /**
     * Отправляет сообщение конкретному пользователю по его идентификатору.
     *
     * @param msg    текст сообщения
     * @param userId уникальный идентификатор получателя
     */
    void sendMessage(String msg, String userId);

    /**
     * Регистрирует пользователя в чат-комнате.
     * После регистрации пользователь может получать адресованные ему сообщения.
     *
     * @param user пользователь, которого нужно добавить
     */
    void addUser(User user);
}
