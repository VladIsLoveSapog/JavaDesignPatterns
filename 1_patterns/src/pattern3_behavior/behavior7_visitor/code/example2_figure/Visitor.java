package pattern3_behavior.behavior7_visitor.code.example2_figure;

/**
 * Интерфейс посетителя геометрических фигур (Visitor в паттерне Visitor).
 *
 * <p>Объявляет метод посещения для каждого типа фигуры.
 * Конкретные посетители (например, {@link PrintVisitor}) реализуют
 * эти методы, определяя свою операцию для каждого типа элемента.
 *
 * <p><b>Расширяемость:</b> чтобы добавить новую операцию над фигурами
 * (например, вычисление площади), достаточно создать нового посетителя —
 * классы фигур изменять не нужно.
 */
public interface Visitor {

    /**
     * Выполняет операцию над кругом.
     *
     * @param circle круг для обработки
     */
    void visitCircle(Circle circle);

    /**
     * Выполняет операцию над прямоугольником.
     *
     * @param rectangle прямоугольник для обработки
     */
    void visitRectangle(Rectangle rectangle);
}
