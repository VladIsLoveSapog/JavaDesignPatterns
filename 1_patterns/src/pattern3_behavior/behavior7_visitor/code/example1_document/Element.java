package pattern3_behavior.behavior7_visitor.code.example1_document;

/**
 * Интерфейс элемента документа (Element в паттерне Visitor).
 *
 * <p>Каждый узел документа (XML, JSON и др.) реализует этот интерфейс,
 * чтобы посетитель мог выполнить над ним нужное действие.
 *
 * <p><b>Роль в паттерне:</b> определяет контракт для всех элементов,
 * которые могут быть «посещены». Метод {@link #accept(DocVisitor)}
 * обеспечивает механизм двойной диспетчеризации — элемент сам вызывает
 * нужный метод посетителя, передавая {@code this}.
 */
public interface Element {

    /**
     * Принимает посетителя и делегирует ему выполнение операции.
     *
     * <p>Реализация в каждом конкретном элементе должна вызвать
     * соответствующий метод посетителя, например:
     * {@code visitor.doForXmlElement(this)} или
     * {@code visitor.doForJSONElement(this)}.
     *
     * @param visitor посетитель, выполняющий операцию над элементом
     */
    void accept(DocVisitor visitor);
}
