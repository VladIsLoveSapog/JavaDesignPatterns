package pattern3_behavior.behavior7_visitor.code.example1_document;

/**
 * Интерфейс посетителя документов (Visitor в паттерне Visitor).
 *
 * <p>Объявляет методы «посещения» для каждого типа элемента документа.
 * Каждый конкретный посетитель (например, {@link SerializeDocVisitor})
 * реализует эти методы, определяя свою операцию над элементами.
 *
 * <p><b>Почему по одному методу на каждый тип элемента:</b> это позволяет
 * посетителю выполнять разную логику для разных типов узлов без
 * использования {@code instanceof} и приведения типов — вся диспетчеризация
 * происходит через полиморфизм (двойная диспетчеризация).
 */
public interface DocVisitor {

    /**
     * Выполняет операцию над XML-элементом документа.
     *
     * @param element XML-узел, подлежащий обработке
     */
    void doForXmlElement(XmlElement element);

    /**
     * Выполняет операцию над JSON-элементом документа.
     *
     * @param element JSON-узел, подлежащий обработке
     */
    void doForJSONElement(JsonElement element);
}
