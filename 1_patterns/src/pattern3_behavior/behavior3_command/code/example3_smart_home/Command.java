package pattern3_behavior.behavior3_command.code.example3_smart_home;

/**
 * Интерфейс команды умного дома (роль <b>Command</b>).
 *
 * <p>Определяет контракт для всех команд управления устройствами:
 * {@link #execute()} — выполнить действие, {@link #undo()} — отменить его.
 *
 * <p>Каждая конкретная команда ({@link LightOnCommand}, {@link AirConditionerSetTempCommand}
 * и др.) инкапсулирует вызов методов конкретного устройства-получателя
 * ({@link Light}, {@link AirConditioner}) и хранит данные, необходимые для отмены.
 *
 * <p><b>Когда использовать:</b> когда разнородные устройства должны
 * управляться через единый интерфейс (например, пульт {@link RemoteControl}),
 * а операции должны поддерживать отмену.
 */
public interface Command {
    /** Выполняет команду над устройством-получателем. */
    void execute();

    /** Отменяет последнее выполнение команды (обратное действие). */
    void undo();
}
