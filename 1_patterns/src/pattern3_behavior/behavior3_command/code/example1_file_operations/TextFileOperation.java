package pattern3_behavior.behavior3_command.code.example1_file_operations;

/**
 * Интерфейс команды для операций с текстовым файлом (роль <b>Command</b>).
 *
 * <p>Определяет единый контракт — метод {@link #execute()} — для всех
 * конкретных операций над файлом. Вызывающий объект ({@link TextFileOperationExecutor})
 * работает с любой операцией через этот интерфейс, не зная деталей реализации.
 *
 * <p><b>Функциональный интерфейс:</b> содержит ровно один абстрактный метод,
 * поэтому помечен аннотацией {@code @FunctionalInterface}. Это позволяет
 * использовать лямбда-выражения и ссылки на методы вместо создания
 * отдельного класса для каждой простой команды (см. демонстрацию в {@link Main}).
 *
 * <p><b>Когда использовать:</b> когда нужно параметризовать объекты
 * операциями, ставить операции в очередь или логировать их, не привязываясь
 * к конкретной реализации.
 */
@FunctionalInterface
public interface TextFileOperation {
    /**
     * Выполняет операцию над текстовым файлом.
     *
     * @return строковое описание результата выполнения операции
     */
    String execute();
}
