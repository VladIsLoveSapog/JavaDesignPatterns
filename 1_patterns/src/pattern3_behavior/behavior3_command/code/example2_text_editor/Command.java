package pattern3_behavior.behavior3_command.code.example2_text_editor;

/**
 * Интерфейс команды с поддержкой отмены (роль <b>Command</b>).
 *
 * <p>В отличие от {@code TextFileOperation} из примера 1, этот интерфейс
 * содержит <b>два</b> метода: {@link #execute()} и {@link #undo()}.
 * Поэтому он <b>не является функциональным интерфейсом</b> и не может быть
 * реализован лямбда-выражением — для каждой конкретной команды необходим
 * полноценный класс.
 *
 * <p><b>Когда использовать:</b> когда операции должны поддерживать отмену.
 * Каждая конкретная команда ({@link InsertCommand}, {@link DeleteCommand})
 * сохраняет достаточно информации для выполнения обратного действия.
 */
public interface Command {
    /** Выполняет команду. */
    void execute();

    /** Отменяет последнее выполнение команды (обратное действие). */
    void undo();
}
