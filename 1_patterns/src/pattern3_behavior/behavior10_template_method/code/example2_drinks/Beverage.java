package pattern3_behavior.behavior10_template_method.code.example2_drinks;

/**
 * Абстрактный класс напитка — <b>AbstractClass</b> паттерна Template Method.
 *
 * <p>Содержит шаблонный метод {@link #prepareRecipe()}, который определяет
 * неизменяемый алгоритм приготовления напитка. Метод объявлен как {@code final},
 * чтобы подклассы не могли изменить порядок шагов.
 *
 * <p><b>Типы методов:</b>
 * <ul>
 *   <li>{@code prepareRecipe()} — шаблонный метод ({@code final})</li>
 *   <li>{@code brew()}, {@code addCondiments()} — абстрактные шаги ({@code protected abstract})</li>
 *   <li>{@code boilWater()}, {@code pourInCup()} — конкретные шаги ({@code private})</li>
 * </ul>
 *
 * @see Tea
 * @see Coffee
 */
abstract class Beverage {

    /**
     * <b>Шаблонный метод</b> — определяет алгоритм приготовления напитка.
     *
     * <p>Последовательность шагов фиксирована:
     * вскипятить воду → заварить → налить в чашку → добавить добавки.
     * Подклассы реализуют только шаги {@link #brew()} и {@link #addCondiments()}.
     */
    public final void prepareRecipe() {
        boilWater();        // конкретный шаг — одинаков для всех напитков
        brew();             // абстрактный шаг — зависит от типа напитка
        pourInCup();        // конкретный шаг — одинаков для всех напитков
        addCondiments();    // абстрактный шаг — зависит от типа напитка
    }

    /**
     * <b>Конкретный шаг</b> — вскипятить воду.
     *
     * <p>Общий для всех напитков, поэтому реализован здесь
     * и объявлен как {@code private} — подклассы не могут переопределить.
     */
    private void boilWater() {
        System.out.println("Кипячение воды");
    }

    /**
     * <b>Конкретный шаг</b> — налить напиток в чашку.
     *
     * <p>Общий для всех напитков, реализован здесь и объявлен как {@code private}.
     */
    private void pourInCup() {
        System.out.println("Наливание в чашку");
    }

    /**
     * <b>Абстрактный шаг</b> — заварить напиток.
     *
     * <p>Каждый подкласс реализует этот метод по-своему:
     * чай заваривается, кофе — варится.
     */
    protected abstract void brew();

    /**
     * <b>Абстрактный шаг</b> — добавить добавки к напитку.
     *
     * <p>Каждый подкласс добавляет свои добавки:
     * лимон для чая, сахар и молоко для кофе.
     */
    protected abstract void addCondiments();
}
