package pattern3_behavior.behavior10_template_method.code.example1_lab;

/**
 * Абстрактный алгоритм сдачи ЛР — <b>AbstractClass</b> паттерна Template Method.
 *
 * <p>Определяет шаблонный метод {@link #pass()}, который задаёт
 * фиксированный порядок шагов сдачи лабораторной работы.
 * Метод объявлен как {@code final}, чтобы подклассы не могли
 * изменить последовательность шагов.
 *
 * <p><b>Хуки:</b> методы {@link #writeOff()} и {@link #correctRemarks()}
 * имеют пустую реализацию по умолчанию. Подклассы переопределяют их
 * только при необходимости (например, если студент списывает).
 *
 * <p><b>Оценка 41</b> — юмористическая деталь: метод {@code pass()}
 * всегда возвращает 41, что означает «еле-еле сдал».
 *
 * @see HonestlyPassSDPLabWork
 * @see NoHonestlyPassSDPLabWork
 */
public abstract class PassLabWork implements IPassLabWork {

    /**
     * <b>Шаблонный метод</b> — определяет неизменяемый алгоритм сдачи ЛР.
     *
     * <p>Порядок шагов фиксирован: списать → написать программу →
     * написать отчёт → показать программу → показать отчёт → исправить замечания.
     * Метод объявлен как {@code final}, поэтому подклассы не могут
     * изменить этот порядок.
     *
     * @return оценка за ЛР (всегда 41 — «еле-еле сдал»)
     */
    @Override
    public final Integer pass() {
        writeOff();         // хук — списали (если нечестная сдача)
        writeProgram();     // абстрактный шаг — «написали» программу
        writeReport();      // абстрактный шаг — «написали» отчёт
        showProgram();      // абстрактный шаг — показали программу
        showReport();       // абстрактный шаг — показали отчёт
        correctRemarks();   // хук — исправили замечания (если есть)
        return 41;          // еле-еле сдал
    }

    /**
     * <b>Хук</b> — списать чужую работу.
     *
     * <p>По умолчанию ничего не делает (пустая реализация).
     * Переопределяется в {@link NoHonestlyPassSDPLabWork},
     * где студент списывает работу.
     */
    @Override
    public void writeOff() {
        // хук — списывать не обязательно :)
    }

    /**
     * <b>Хук</b> — исправить замечания преподавателя.
     *
     * <p>По умолчанию ничего не делает (пустая реализация).
     * Переопределяется в {@link NoHonestlyPassSDPLabWork},
     * где преподаватель замечает списывание и отправляет на переделку.
     */
    @Override
    public void correctRemarks() {
        // хук — возможно, всё хорошо с первого раза
    }
}
