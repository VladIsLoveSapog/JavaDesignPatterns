package pattern3_behavior.behavior10_template_method.code.example1_lab;

/**
 * Интерфейс алгоритма сдачи лабораторной работы — описывает контракт
 * для паттерна <b>Template Method</b>.
 *
 * <p>Определяет все шаги, которые входят в процесс сдачи ЛР.
 * Реализация шаблонного метода {@link #pass()} и хуков находится
 * в абстрактном классе {@link PassLabWork}.
 *
 * @see PassLabWork
 * @see HonestlyPassSDPLabWork
 * @see NoHonestlyPassSDPLabWork
 */
public interface IPassLabWork {

    /**
     * Шаблонный метод — сдача лабораторной работы.
     *
     * <p>Выполняет все шаги алгоритма в фиксированном порядке
     * и возвращает итоговую оценку.
     *
     * @return оценка за лабораторную работу
     */
    Integer pass();

    /**
     * <b>Хук</b> — списать чужую работу.
     *
     * <p>Необязательный шаг: по умолчанию ничего не делает
     * (не все студенты списывают).
     */
    void writeOff();

    /**
     * <b>Абстрактный шаг</b> — написать программу.
     *
     * <p>Каждый подкласс реализует этот метод по-своему:
     * честно или на основе списанного кода.
     */
    void writeProgram();

    /**
     * <b>Абстрактный шаг</b> — написать отчёт по лабораторной работе.
     */
    void writeReport();

    /**
     * <b>Абстрактный шаг</b> — показать программу преподавателю.
     */
    void showProgram();

    /**
     * <b>Абстрактный шаг</b> — показать отчёт преподавателю.
     */
    void showReport();

    /**
     * <b>Хук</b> — исправить замечания преподавателя.
     *
     * <p>Необязательный шаг: если работа выполнена честно,
     * замечаний может не быть.
     */
    void correctRemarks();
}
