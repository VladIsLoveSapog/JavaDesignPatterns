package pattern3_behavior.behavior2_strategy.code.example1_navigator;

/**
 * Стратегия автомобильного маршрута (роль <b>ConcreteStrategy</b>).
 *
 * <p>Реализует алгоритм построения маршрута для автомобиля.
 * В реальном приложении здесь мог бы использоваться граф дорог
 * с учётом пробок, платных участков и ограничений скорости.
 *
 * <p><b>Когда использовать:</b> пользователь выбирает поездку на автомобиле —
 * {@link Navigator} получает эту стратегию и делегирует ей построение маршрута.
 */
public class RoadStrategy implements RouteStrategy {

    /**
     * Строит автомобильный маршрут между двумя точками.
     *
     * @param start начальная точка маршрута
     * @param end   конечная точка маршрута
     * @return описание построенного автомобильного маршрута
     */
    @Override
    public String buildRoute(String start, String end) {
        return "От точки " + start + " построен автомобильный маршрут до точки " + end;
    }
}
