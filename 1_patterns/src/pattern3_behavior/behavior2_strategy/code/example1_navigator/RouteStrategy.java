package pattern3_behavior.behavior2_strategy.code.example1_navigator;

/**
 * Интерфейс стратегии построения маршрута (роль <b>Strategy</b>).
 *
 * <p>Определяет единый контракт для всех алгоритмов построения маршрута.
 * Контекст ({@link Navigator}) работает с любой стратегией исключительно
 * через этот интерфейс, не зная деталей конкретной реализации.
 *
 * <p><b>Почему интерфейс, а не абстрактный класс:</b> стратегии маршрута
 * не имеют общего состояния или общей логики, которую нужно наследовать.
 * Интерфейс даёт максимальную гибкость: конкретная стратегия может
 * наследовать другой класс (например, сетевой клиент для онлайн-карт)
 * и при этом реализовать {@code RouteStrategy}.
 *
 * <p><b>Когда использовать:</b> когда алгоритм построения маршрута может
 * меняться в зависимости от выбора пользователя (автомобиль, автобус, пешком)
 * и вы хотите переключать его на лету без изменения клиентского кода.
 */
public interface RouteStrategy {
    /**
     * Строит маршрут между двумя точками.
     *
     * @param start начальная точка маршрута
     * @param end   конечная точка маршрута
     * @return текстовое описание построенного маршрута
     */
    String buildRoute(String start, String end);
}
