package pattern3_behavior.behavior2_strategy.code.example1_navigator;

/**
 * Стратегия маршрута на общественном транспорте (роль <b>ConcreteStrategy</b>).
 *
 * <p>Реализует алгоритм построения маршрута с использованием автобусов,
 * трамваев, метро и других видов общественного транспорта.
 * В реальном приложении алгоритм учитывал бы расписания, пересадки
 * и стоимость проезда.
 *
 * <p><b>Когда использовать:</b> пользователь выбирает поездку на общественном
 * транспорте — {@link Navigator} переключается на эту стратегию.
 */
public class PublicTransportStrategy implements RouteStrategy {

    /**
     * Строит маршрут на общественном транспорте между двумя точками.
     *
     * @param start начальная точка маршрута
     * @param end   конечная точка маршрута
     * @return описание построенного маршрута на общественном транспорте
     */
    @Override
    public String buildRoute(String start, String end) {
        return "От точки " + start + " построен маршрут с использованием общественного транспорта до точки " + end;
    }
}
