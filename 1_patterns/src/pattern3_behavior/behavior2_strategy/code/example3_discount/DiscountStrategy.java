package pattern3_behavior.behavior2_strategy.code.example3_discount;

/**
 * Интерфейс стратегии скидки (роль <b>Strategy</b>).
 *
 * <p>Определяет единый контракт для всех алгоритмов расчёта скидки.
 * Контекст ({@link Order}) работает с любой стратегией скидки исключительно
 * через этот интерфейс, не зная деталей конкретной реализации.
 *
 * <p>Это <b>функциональный интерфейс</b> (содержит ровно один абстрактный метод),
 * поэтому начиная с Java 8 его можно реализовать лямбда-выражением:
 * <pre>{@code
 * DiscountStrategy custom = price -> price * 0.10;
 * }</pre>
 *
 * <p><b>Когда использовать:</b> когда магазин предлагает разные виды скидок
 * (обычная покупка, студенческая, VIP, промокод) и вы хотите переключать
 * логику расчёта на лету без изменения класса заказа.
 */
public interface DiscountStrategy {
    /**
     * Рассчитывает размер скидки для указанной цены.
     *
     * @param price исходная цена товара (до скидки)
     * @return сумма скидки (≥ 0)
     */
    double calculateDiscount(double price);
}
