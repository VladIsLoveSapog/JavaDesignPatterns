package pattern3_behavior.behavior2_strategy.code.example2_sort;

import java.util.List;

/**
 * Интерфейс стратегии сортировки (роль <b>Strategy</b>).
 *
 * <p>Определяет единый контракт — метод {@link #sort(List)} — для всех
 * алгоритмов сортировки списка строк. Контекст ({@link Sorter}) работает
 * с любой стратегией через этот интерфейс, не зная деталей реализации.
 *
 * <p><b>Контракт:</b> реализация должна отсортировать переданный список
 * <i>in-place</i> (изменяя сам список) и вывести результат в консоль.
 *
 * <p><b>Когда использовать:</b> когда один и тот же набор данных нужно
 * сортировать разными способами, а выбор алгоритма определяется
 * пользователем или контекстом во время выполнения.
 */
public interface SortStrategy {
    /**
     * Сортирует переданный список строк и выводит результат.
     *
     * @param items список строк для сортировки (изменяется in-place)
     */
    void sort(List<String> items);
}
