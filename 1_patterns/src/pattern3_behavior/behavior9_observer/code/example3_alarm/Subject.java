package pattern3_behavior.behavior9_observer.code.example3_alarm;

/**
 * Интерфейс издателя (Subject) в паттерне «Наблюдатель» для системы мониторинга температуры.
 *
 * <p>Определяет контракт для управления подпиской наблюдателей ({@link Observer})
 * и их оповещения при изменении состояния. Любой класс, реализующий этот интерфейс,
 * берёт на себя роль <b>издателя</b> (Observable), способного рассылать уведомления
 * зарегистрированным подписчикам.
 *
 * <p><b>Роль в паттерне Observer:</b><br>
 * Это интерфейс <i>Subject</i> — абстракция издателя. Конкретная реализация —
 * {@link TemperatureSensor}, который хранит список наблюдателей и уведомляет их
 * при каждом изменении температуры.
 *
 * <p><b>Методы интерфейса:</b>
 * <ol>
 *   <li>{@link #addObserver(Observer)} — регистрация нового наблюдателя</li>
 *   <li>{@link #removeObserver(Observer)} — удаление наблюдателя из подписки</li>
 *   <li>{@link #notifyObservers()} — оповещение всех зарегистрированных наблюдателей</li>
 * </ol>
 *
 * @see Observer
 * @see TemperatureSensor
 */
interface Subject {

    /**
     * Регистрирует нового наблюдателя для получения уведомлений.
     *
     * <p>После вызова этого метода переданный {@link Observer} будет получать
     * обратные вызовы {@link Observer#update(float)} при каждом изменении
     * состояния издателя.
     *
     * @param observer наблюдатель, которого необходимо подписать на уведомления
     */
    void addObserver(Observer observer);

    /**
     * Удаляет наблюдателя из списка подписчиков.
     *
     * <p>После удаления наблюдатель перестаёт получать уведомления
     * об изменении состояния издателя.
     *
     * @param observer наблюдатель, которого необходимо отписать от уведомлений
     */
    void removeObserver(Observer observer);

    /**
     * Оповещает всех зарегистрированных наблюдателей об изменении состояния.
     *
     * <p>Вызывает метод {@link Observer#update(float)} у каждого подписанного
     * наблюдателя, передавая актуальное значение температуры.
     */
    void notifyObservers();
}
