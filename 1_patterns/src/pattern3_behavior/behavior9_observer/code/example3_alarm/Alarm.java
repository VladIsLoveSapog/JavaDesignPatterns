package pattern3_behavior.behavior9_observer.code.example3_alarm;

/**
 * Конкретный наблюдатель (ConcreteObserver) — сигнализация превышения температуры.
 *
 * <p>Реализует интерфейс {@link Observer} и срабатывает <b>только</b> при превышении
 * заданного порога температуры. В отличие от {@link Display}, содержит
 * <b>условную логику</b>: предупреждение выводится лишь тогда, когда температура
 * превышает <i>30.0°C</i>.
 *
 * <p><b>Роль в паттерне Observer:</b><br>
 * Это <i>ConcreteObserver</i> с фильтрацией событий. Демонстрирует, что
 * разные наблюдатели могут по-разному реагировать на одно и то же уведомление:
 * {@link Display} отображает любую температуру, а {@code Alarm} — только
 * критические значения.
 *
 * <p><b>Пороговое значение:</b> {@code 30.0f} °C. Если температура строго
 * больше этого значения, на консоль выводится сообщение:
 * <pre>
 *     Сигнал тревоги! Высокая температура: {temperature}°C
 * </pre>
 *
 * @see Observer
 * @see Subject
 * @see TemperatureSensor
 * @see Display
 */
class Alarm implements Observer {

    /**
     * Получает уведомление об изменении температуры и при превышении порога
     * выводит сигнал тревоги.
     *
     * <p>Условие срабатывания: {@code temperature > 30.0f}. Если температура
     * не превышает пороговое значение, метод не выполняет никаких действий.
     *
     * @param temperature текущая температура в градусах Цельсия,
     *                    полученная от {@link TemperatureSensor}
     */
    @Override
    public void update(float temperature){
        if(temperature > 30.0f){
            System.out.println("Сигнал тревоги! Высокая температура: " + temperature + "°C");
        }
    }
}
