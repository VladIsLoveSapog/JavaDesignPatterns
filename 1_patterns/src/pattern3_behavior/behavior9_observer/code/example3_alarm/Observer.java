package pattern3_behavior.behavior9_observer.code.example3_alarm;

/**
 * Интерфейс наблюдателя (Observer) в паттерне «Наблюдатель» для системы мониторинга температуры.
 *
 * <p>Определяет контракт для всех объектов, которые хотят получать уведомления
 * об изменении температуры от датчика ({@link TemperatureSensor}). Использует
 * <b>push-модель</b> передачи данных — температура передаётся непосредственно
 * в параметре метода {@link #update(float)}.
 *
 * <p><b>Роль в паттерне Observer:</b><br>
 * Это интерфейс <i>Observer</i> — абстракция для всех конкретных наблюдателей.
 * Реализации: {@link Display} (отображение) и {@link Alarm} (сигнализация).
 *
 * @see Subject
 * @see TemperatureSensor
 * @see Display
 * @see Alarm
 */
interface Observer {

    /**
     * Вызывается издателем при изменении температуры.
     *
     * <p>Каждый конкретный наблюдатель реализует собственную логику реакции:
     * {@link Display} — отображает значение, {@link Alarm} — проверяет
     * превышение порога и выводит предупреждение.
     *
     * @param temperature текущая температура, полученная от датчика
     */
    void update(float temperature);
}


