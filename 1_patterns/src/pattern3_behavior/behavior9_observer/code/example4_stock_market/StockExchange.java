package pattern3_behavior.behavior9_observer.code.example4_stock_market;

/**
 * Интерфейс издателя (Subject) в паттерне «Наблюдатель» для биржевой системы.
 *
 * <p>Определяет контракт для управления подписками наблюдателей
 * ({@link StockObserver}). Любой объект, реализующий этот интерфейс,
 * может регистрировать, удалять и оповещать наблюдателей об изменениях.
 *
 * <p><b>Роль в паттерне Observer:</b><br>
 * Это интерфейс <i>Subject</i> — абстракция издателя. Конкретная реализация —
 * {@link Stock}, представляющая отдельную акцию на бирже.
 *
 * <p><b>Отличие от example3_alarm:</b> в этом примере может существовать
 * несколько независимых издателей (акций), и наблюдатели могут подписываться
 * на разные акции, что демонстрирует работу с множественными Subject'ами.
 *
 * @see StockObserver
 * @see Stock
 */
interface StockExchange {

    /**
     * Регистрирует наблюдателя для получения уведомлений об изменении цены.
     *
     * @param observer наблюдатель, который будет зарегистрирован
     */
    void registerObserver(StockObserver observer);

    /**
     * Удаляет наблюдателя из списка подписчиков.
     *
     * <p>После удаления наблюдатель перестаёт получать уведомления
     * об изменении цены данной акции.
     *
     * @param observer наблюдатель, которого необходимо отписать
     */
    void removeObserver(StockObserver observer);

    /**
     * Оповещает всех зарегистрированных наблюдателей об изменении цены.
     *
     * <p>Вызывается автоматически при изменении состояния издателя
     * (например, при вызове {@link Stock#setPrice(double)}).
     */
    void notifyObservers();
}
