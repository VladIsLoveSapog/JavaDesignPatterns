package pattern3_behavior.behavior1_chain.code.example5_discount;

/**
 * <p><b>Интерфейс обработчика скидок — определяет контракт для всех узлов цепочки.</b></p>
 *
 * <p>Каждый обработчик в цепочке проверяет определённое условие и, если оно выполнено,
 * начисляет скидку на объект {@link Order}. В отличие от классической цепочки обязанностей,
 * где обработка прерывается после первого подходящего обработчика, здесь запрос
 * <b>всегда</b> передаётся следующему узлу — скидки накапливаются.</p>
 *
 * <p>Реализации:</p>
 * <ul>
 *     <li>{@link NewCustomerDiscount} — скидка для новых покупателей</li>
 *     <li>{@link BulkPurchaseDiscount} — скидка за крупный заказ</li>
 *     <li>{@link PromoCodeDiscount} — скидка по промокоду</li>
 * </ul>
 *
 * @see BaseDiscountHandler
 * @see Order
 */
public interface DiscountHandler {

    /**
     * Устанавливает следующий обработчик в цепочке.
     *
     * @param next следующий обработчик скидок
     */
    void setNext(DiscountHandler next);

    /**
     * Применяет логику начисления скидки к переданному заказу
     * и передаёт его дальше по цепочке.
     *
     * @param order заказ, к которому применяется скидка
     */
    void applyDiscount(Order order);
}
