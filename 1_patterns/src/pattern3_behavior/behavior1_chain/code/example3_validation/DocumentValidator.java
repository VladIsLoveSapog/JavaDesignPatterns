package pattern3_behavior.behavior1_chain.code.example3_validation;

/**
 * <p><b>Интерфейс обработчика (Handler)</b> в паттерне «Цепочка обязанностей».</p>
 *
 * <p>Определяет контракт для всех валидаторов документов в цепочке:
 * возможность установить следующий валидатор и выполнить валидацию
 * переданного документа ({@link Document}).</p>
 *
 * <p>Каждый валидатор самостоятельно решает, способен ли он обработать
 * документ данного типа. Если не способен — передаёт его следующему
 * звену цепочки. Если следующего звена нет, поведение определяется
 * конкретной реализацией (например, {@link TerminalValidator} выбрасывает
 * исключение {@link UnsupportedDocumentException}).</p>
 *
 * @see BaseValidator
 * @see TerminalValidator
 * @see Document
 */
public interface DocumentValidator {

    /**
     * Устанавливает следующий валидатор в цепочке.
     *
     * @param next следующий валидатор, которому будет передан документ,
     *             если текущий не сможет его обработать
     */
    void setNext(DocumentValidator next);

    /**
     * Выполняет валидацию документа.
     *
     * <p>Если валидатор способен обработать документ данного типа —
     * он выполняет валидацию. В противном случае передаёт документ
     * следующему валидатору в цепочке.</p>
     *
     * @param document документ для валидации
     */
    void validate(Document document);
}
