package pattern3_behavior.behavior1_chain.code.example1_message;

/**
 * Интерфейс обработчика в паттерне «Цепочка обязанностей» (Chain of Responsibility).
 *
 * <p>Данный интерфейс должны реализовывать все обработчики в цепочке.
 * Каждый обработчик решает, может ли он обработать запрос самостоятельно,
 * и при необходимости передаёт его следующему звену через {@link #setNext(Handler)}.
 *
 * <p><b>Роль в паттерне:</b> определяет единый контракт для всех конкретных
 * обработчиков ({@link MessageSender} и его наследников), позволяя клиентскому
 * коду работать с цепочкой через абстракцию, не зная конкретных реализаций.
 *
 * @see MessageSender
 * @see PriorityLevel
 */
public interface Handler {

    /**
     * Устанавливает следующий обработчик в цепочке.
     *
     * <p>После вызова этого метода текущий обработчик при необходимости
     * будет делегировать обработку запроса переданному {@code handler}.
     *
     * @param handler следующий обработчик в цепочке
     */
    void setNext(Handler handler);

    /**
     * Обрабатывает запрос или передаёт его следующему обработчику в цепочке.
     *
     * <p>Конкретная логика обработки определяется реализацией. Обработчик
     * может выполнить действие, пропустить запрос или передать его дальше
     * в зависимости от уровня приоритета {@code level}.
     *
     * @param message текст сообщения для обработки
     * @param level   уровень приоритета сообщения
     */
    void handle(String message, PriorityLevel level);

}
