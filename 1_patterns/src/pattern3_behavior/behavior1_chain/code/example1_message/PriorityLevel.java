package pattern3_behavior.behavior1_chain.code.example1_message;

/**
 * Уровень приоритетности сообщения в цепочке обязанностей.
 *
 * <p>Определяет, какие обработчики ({@link MessageSender}) должны обработать
 * сообщение. Обработчик срабатывает, если его собственный уровень приоритета
 * не превышает уровень сообщения (сравнение через {@link #ordinal()}).
 *
 * <p><b>Порядок приоритетов (от низкого к высокому):</b>
 * <ol>
 *   <li>{@link #LOW} — низкий приоритет (запись в лог)</li>
 *   <li>{@link #MIDDLE} — средний приоритет (отправка email)</li>
 *   <li>{@link #HIGH} — высокий приоритет (отправка SMS)</li>
 * </ol>
 *
 * @see Handler#handle(String, PriorityLevel)
 * @see MessageSender
 */
public enum PriorityLevel {

    /** Низкий приоритет — сообщение записывается только в лог. */
    LOW,

    /** Средний приоритет — помимо лога, отправляется уведомление по email. */
    MIDDLE,

    /** Высокий приоритет — задействуются все обработчики, включая SMS. */
    HIGH
}
