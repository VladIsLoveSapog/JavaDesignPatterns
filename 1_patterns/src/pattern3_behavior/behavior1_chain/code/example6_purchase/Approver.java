package pattern3_behavior.behavior1_chain.code.example6_purchase;

/**
 * Интерфейс обработчика в паттерне "Цепочка обязанностей" (Chain of Responsibility).
 *
 * <p>Определяет контракт для всех участников цепочки одобрения закупок.
 * Каждый обработчик решает, может ли он одобрить запрос {@link PurchaseRequest}
 * самостоятельно, и при необходимости передаёт его следующему звену
 * через {@link #setNext(Approver)}.
 *
 * <p><b>Роль в паттерне:</b> задаёт единый интерфейс для всех конкретных
 * обработчиков ({@link TeamLead}, {@link DepartmentHead}, {@link Director}),
 * позволяя клиентскому коду работать с цепочкой через абстракцию,
 * не зная конкретных реализаций.
 *
 * <p><b>Классическая цепочка обязанностей:</b> в данном примере запрос обрабатывается
 * ровно одним узлом — тем, чей лимит достаточен для одобрения. Остальные узлы
 * просто передают запрос дальше по цепочке.
 *
 * @see BaseApprover
 * @see PurchaseRequest
 */
public interface Approver {

    /**
     * Устанавливает следующий обработчик в цепочке одобрения.
     *
     * <p>После вызова этого метода текущий обработчик при необходимости
     * будет делегировать одобрение запроса переданному {@code next}.
     *
     * @param next следующий обработчик в цепочке
     */
    void setNext(Approver next);

    /**
     * Рассматривает запрос на закупку и принимает решение об одобрении.
     *
     * <p>Если сумма запроса не превышает лимит текущего обработчика — запрос
     * одобряется и цепочка останавливается. В противном случае запрос
     * передаётся следующему обработчику, установленному через {@link #setNext(Approver)}.
     *
     * @param request запрос на закупку, содержащий описание и сумму
     */
    void approve(PurchaseRequest request);
}
