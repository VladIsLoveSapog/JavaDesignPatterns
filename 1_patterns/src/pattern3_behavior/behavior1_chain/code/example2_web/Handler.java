package pattern3_behavior.behavior1_chain.code.example2_web;

/**
 * <p><b>Интерфейс обработчика</b> в паттерне «Цепочка обязанностей».</p>
 *
 * <p>Определяет контракт для всех обработчиков в цепочке:
 * возможность установить следующий обработчик и обработать запрос.</p>
 *
 * <p>Каждый обработчик решает самостоятельно, может ли он обработать
 * входящий запрос. Если не может — передаёт его следующему звену цепочки.
 * Если следующего звена нет, обработка завершается.</p>
 *
 * @see AbstractHandler
 * @see AuthenticationHandler
 * @see AuthorizationHandler
 * @see LoggingHandler
 */
public interface Handler {

    /**
     * Устанавливает следующий обработчик в цепочке.
     *
     * @param handler следующий обработчик, которому будет передан запрос,
     *                если текущий не сможет его обработать
     */
    void setNextHandler(Handler handler);

    /**
     * Обрабатывает входящий запрос.
     *
     * <p>Если обработчик способен обработать запрос — он выполняет обработку
     * и, при необходимости, передаёт преобразованный запрос дальше по цепочке.
     * Если обработчик не может обработать запрос — он передаёт его без изменений
     * следующему обработчику.</p>
     *
     * @param request строковое представление запроса
     */
    void handleRequest(String request);
}
