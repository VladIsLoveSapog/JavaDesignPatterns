package pattern3_behavior.behavior1_chain.code.example4_logging;

/**
 * <p><b>Интерфейс обработчика (Handler)</b> в паттерне «Цепочка обязанностей».</p>
 *
 * <p>Определяет контракт для всех обработчиков в конвейере логирования:
 * возможность установить следующий обработчик и выполнить обработку
 * записи лога ({@link LogRecord}).</p>
 *
 * <p>В отличие от классической реализации паттерна, где каждый обработчик
 * может прервать цепочку, данный конвейер <b>всегда проходится полностью</b>.
 * Каждый обработчик выполняет свою часть обработки (добавление метки времени,
 * форматирование, фильтрация, вывод) и безусловно передаёт запись
 * следующему звену.</p>
 *
 * @see BaseLogProcessor
 * @see TimestampProcessor
 * @see FormatProcessor
 * @see FilterProcessor
 * @see ConsoleOutputProcessor
 */
public interface LogProcessor {

    /**
     * Устанавливает следующий обработчик в цепочке.
     *
     * @param next следующий обработчик, которому будет передана запись лога
     *             после обработки текущим обработчиком
     */
    void setNext(LogProcessor next);

    /**
     * Обрабатывает запись лога.
     *
     * <p>Каждый обработчик выполняет свою часть обработки записи
     * и <b>всегда</b> передаёт её следующему обработчику в цепочке,
     * если таковой существует. Цепочка никогда не прерывается.</p>
     *
     * @param record запись лога для обработки
     */
    void process(LogRecord record);
}
