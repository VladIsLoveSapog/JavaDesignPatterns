package pattern2_structural.struct3_bridge.code.example2_devices;

/**
 * Продвинутый пульт управления — <b>Уточнённая абстракция</b> (Refined Abstraction)
 * в паттерне Bridge.
 *
 * <p>Расширяет базовый {@link Remote} дополнительной функцией {@link #mute()},
 * при этом не затрагивая иерархию устройств ({@link Device}, {@link TV}, {@link Radio}).
 *
 * <p><b>Почему это работает без изменений в устройствах:</b> метод {@code mute()}
 * использует уже существующий контракт {@link Device#setVolume(Integer)}.
 * Новая функциональность добавляется только на стороне Абстракции —
 * это главное преимущество паттерна Bridge: обе иерархии развиваются независимо.
 *
 * <p><b>Почему класс не абстрактный:</b> {@code AdvancedRemote} — готовый к
 * использованию пульт, который можно инстанцировать напрямую. Абстрактным он
 * был бы, если бы содержал нереализованные методы, предназначенные для дальнейшего
 * уточнения в подклассах.
 *
 * @see Remote
 * @see Device
 */
public class AdvancedRemote extends Remote {

    /**
     * Создаёт продвинутый пульт, привязанный к указанному устройству.
     *
     * <p>Передаёт {@code device} в конструктор родительского класса {@link Remote#Remote(Device)},
     * который сохраняет ссылку на устройство в поле {@link Remote#device}.
     *
     * @param device устройство, которым будет управлять этот пульт
     */
    public AdvancedRemote(Device device) {
        super(device);
    }

    /**
     * Отключает звук устройства, устанавливая громкость в {@code 0}.
     *
     * <p>Использует {@link Device#setVolume(Integer)} — не требует специальной
     * поддержки со стороны устройства. Это пример того, как Уточнённая абстракция
     * добавляет функциональность, комбинируя уже имеющиеся операции Реализации.
     */
    void mute() {
        device.setVolume(0);
    }
}
