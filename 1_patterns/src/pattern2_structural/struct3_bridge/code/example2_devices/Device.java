package pattern2_structural.struct3_bridge.code.example2_devices;

/**
 * Интерфейс устройства — <b>Реализация</b> (Implementation) в паттерне Bridge.
 *
 * <p>Определяет низкоуровневый контракт работы с устройством: включение/выключение,
 * управление громкостью и каналом. Конкретные устройства ({@link TV}, {@link Radio})
 * реализуют этот интерфейс, предоставляя собственную логику.
 *
 * <p><b>Почему интерфейс, а не абстрактный класс:</b> у устройств нет общего состояния,
 * которое нужно наследовать. Интерфейс даёт максимальную гибкость — конкретные
 * реализации могут наследовать любой другой класс и при этом оставаться «устройством».
 *
 * <p><b>Связь с Абстракцией:</b> {@link Remote} хранит ссылку на {@code Device}
 * и делегирует ему все операции. Это и есть «мост» — Абстракция (пульт) и Реализация
 * (устройство) развиваются независимо друг от друга.
 */
public interface Device {

    /**
     * Проверяет, включено ли устройство.
     *
     * @return {@code true}, если устройство включено; {@code false} — если выключено
     */
    boolean isEnable();

    /**
     * Включает устройство.
     *
     * <p>После вызова метод {@link #isEnable()} должен возвращать {@code true}.
     */
    void enable();

    /**
     * Выключает устройство.
     *
     * <p>После вызова метод {@link #isEnable()} должен возвращать {@code false}.
     */
    void disable();

    /**
     * Возвращает текущий уровень громкости устройства.
     *
     * @return текущая громкость (например, от 0 до 100)
     */
    Integer getVolume();

    /**
     * Устанавливает уровень громкости устройства.
     *
     * @param volume новое значение громкости
     */
    void setVolume(Integer volume);

    /**
     * Возвращает номер текущего канала устройства.
     *
     * @return номер текущего канала
     */
    Integer getChannel();

    /**
     * Переключает устройство на указанный канал.
     *
     * @param channel номер нового канала
     */
    void setChannel(Integer channel);
}
