package pattern2_structural.struct3_bridge.code.example3_notifications;

/**
 * <b>Роль в паттерне Bridge: Implementation (Реализация).</b>
 *
 * <p>Интерфейс канала доставки сообщений. Определяет единственный метод
 * {@link #send(String, String)}, который принимает уже отформатированное
 * сообщение и отправляет его получателю через конкретный канал (Email, SMS, Telegram и т.д.).
 *
 * <p><b>Почему интерфейс, а не абстрактный класс:</b> у канала доставки нет
 * общего состояния — каждая реализация самостоятельно решает, как именно
 * отправить сообщение. Интерфейс здесь даёт максимальную гибкость:
 * конкретные реализации могут наследовать любые другие классы.
 *
 * <p><b>Ключевой принцип:</b> реализация ничего не знает о формате сообщения
 * (текст, HTML и т.д.) — она получает уже готовую строку {@code formattedMessage}.
 * Формат определяется на стороне Abstraction ({@link Message}).
 */
public interface MessageSender {

    /**
     * Отправляет отформатированное сообщение указанному получателю.
     *
     * @param recipient        адрес/номер/идентификатор получателя
     * @param formattedMessage сообщение, уже отформатированное стороной Abstraction
     */
    void send(String recipient, String formattedMessage);
}
