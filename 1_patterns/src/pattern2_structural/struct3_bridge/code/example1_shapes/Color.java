package pattern2_structural.struct3_bridge.code.example1_shapes;

/**
 * Интерфейс цвета — <b>Implementation</b> (Реализация) в паттерне Bridge.
 *
 * <p>Определяет контракт для всех конкретных реализаций цвета
 * ({@link BlackColor}, {@link GreenColor}, {@link RedColor} и т.д.).
 *
 * <p><b>Почему интерфейс, а не абстрактный класс:</b> у реализаций цвета
 * нет общего состояния (полей), которое нужно наследовать. Интерфейс
 * позволяет любому классу реализовать {@code Color}, не ограничивая его
 * единственным родительским классом — это даёт максимальную гибкость.
 *
 * <p><b>Роль в паттерне Bridge:</b> иерархия реализации (Implementation)
 * развивается независимо от иерархии абстракции ({@link Shape}).
 * Благодаря этому можно добавлять новые цвета, не трогая код фигур,
 * и наоборот — добавлять новые фигуры, не трогая код цветов.
 *
 * @see Shape
 * @see BlackColor
 * @see GreenColor
 * @see RedColor
 */
public interface Color {

    /**
     * Выполняет заливку текущим цветом.
     *
     * <p>Конкретные реализации определяют, каким именно цветом будет
     * закрашена фигура. Метод вызывается из {@link Shape#draw()},
     * что и образует «мост» между абстракцией и реализацией.
     */
    void fillColor();
}
