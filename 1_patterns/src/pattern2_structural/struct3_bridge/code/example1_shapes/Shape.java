package pattern2_structural.struct3_bridge.code.example1_shapes;

/**
 * Абстрактная фигура — <b>Abstraction</b> (Абстракция) в паттерне Bridge.
 *
 * <p>Описывает высокоуровневый интерфейс для работы с фигурами и хранит
 * ссылку на объект {@link Color}, которому делегирует детали раскраски.
 * Именно эта ссылка и является «мостом» (bridge) между двумя независимыми
 * иерархиями: иерархией фигур и иерархией цветов.
 *
 * <p><b>Почему абстрактный класс, а не интерфейс:</b> абстракция хранит
 * поле {@link #color}, которое является общим для всех фигур. Интерфейс
 * в Java не позволяет объявить нестатическое поле с {@code protected}
 * доступом, поэтому используется абстрактный класс.
 *
 * <p><b>Роль в паттерне Bridge:</b> абстракция определяет «что делать»
 * (нарисовать фигуру), а реализация ({@link Color}) определяет «как
 * именно это сделать» (каким цветом закрасить). Конкретные подклассы
 * ({@link Rectangle}, {@link Triangle}) расширяют абстракцию, но по-прежнему
 * делегируют раскраску объекту {@code Color}.
 *
 * @see Color
 * @see Rectangle
 * @see Triangle
 */
public abstract class Shape {

    /**
     * Ссылка на реализацию цвета — это и есть «мост» (bridge).
     *
     * <p>{@code protected} — чтобы подклассы ({@link Rectangle}, {@link Triangle})
     * могли напрямую вызывать {@link Color#fillColor()} в своей реализации
     * метода {@link #draw()}.
     *
     * <p>Тип поля — интерфейс {@link Color}, а не конкретный класс, поэтому
     * любая реализация цвета ({@link BlackColor}, {@link GreenColor},
     * {@link RedColor}) может быть подставлена без изменения кода фигуры.
     */
    protected Color color;

    /**
     * Конструктор принимает реализацию цвета через Dependency Injection.
     *
     * <p><b>Почему через конструктор:</b> цвет является обязательной
     * зависимостью фигуры — без него фигуру невозможно нарисовать.
     * Передача через конструктор гарантирует, что объект {@code Shape}
     * никогда не окажется в некорректном состоянии (без цвета).
     *
     * @param color конкретная реализация цвета ({@link Color}),
     *              которая будет использоваться при отрисовке фигуры
     */
    public Shape(Color color) {
        this.color = color;
    }

    /**
     * Отрисовывает фигуру и заливает её цветом.
     *
     * <p>Каждый подкласс ({@link Rectangle}, {@link Triangle}) реализует
     * собственную логику рисования, но делегирует заливку объекту
     * {@link #color} через вызов {@link Color#fillColor()}.
     */
    public abstract void draw();
}
