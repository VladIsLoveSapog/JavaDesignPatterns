package pattern2_structural.struct7_flyweight.code.example1_menu;

/**
 * Паттерн Легковес (Flyweight) — интерфейс легковеса (Flyweight).
 *
 * <p>Определяет общий интерфейс для всех позиций меню ресторана.
 * Конкретные легковесы ({@link Pizza}, {@link Salad}) реализуют этот интерфейс
 * и хранят внутри себя только <b>внутреннее (разделяемое) состояние</b> — название блюда.
 *
 * <p>Внешнее состояние (номер стола) передаётся через параметр метода {@link #serve(Integer)},
 * что позволяет одному и тому же экземпляру блюда обслуживать разные столы.
 */
public interface MenuEntry {
    /**
     * Подаёт блюдо на указанный стол.
     *
     * @param tableNumber номер стола, на который подаётся блюдо (внешнее состояние)
     */
    void serve(Integer tableNumber);
}
