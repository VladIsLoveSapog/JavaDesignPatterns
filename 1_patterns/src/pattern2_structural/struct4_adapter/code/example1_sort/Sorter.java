package pattern2_structural.struct4_adapter.code.example1_sort;

/**
 * Целевой интерфейс (Target) — контракт, который ожидает клиентский код.
 *
 * <p>В терминах паттерна Адаптер это интерфейс, к которому мы хотим
 * привести несовместимый внешний API. Клиентский код работает только
 * с этим интерфейсом и ничего не знает о внутренней реализации.
 *
 * <p>В данном примере целевой интерфейс определяет контракт для сортировки
 * массива примитивных {@code int}. Наша система ожидает именно такой формат,
 * но стороннее решение работает с {@code List<Integer>}.
 */
public interface Sorter {

    /**
     * Сортирует переданный массив целых чисел по возрастанию.
     *
     * @param numbers массив целых чисел (varargs — можно передать как массив,
     *                так и перечисление чисел через запятую)
     * @return отсортированный массив
     */
    int[] sort(int... numbers);
}