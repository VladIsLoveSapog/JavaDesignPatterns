package pattern2_structural.struct1_composite.code.example1_product_boxes;

/**
 * Общий интерфейс элементов дерева (роль <b>Component</b> в паттерне Composite).
 *
 * <p>Определяет единый контракт для всех узлов иерархии — как для
 * конечных элементов ({@link Leaf}), так и для контейнеров ({@link Container}).
 * Благодаря этому клиентский код может работать с любым узлом дерева
 * единообразно, не зная его конкретный тип.
 *
 * <p><b>Ключевой метод:</b> {@link #calcCost()} — общее действие
 * (в классической диаграмме обычно называется {@code execute()}).
 * Для листа он возвращает собственную стоимость, а для контейнера —
 * рекурсивно вычисленную сумму стоимостей всех дочерних элементов.
 *
 * @see Container
 * @see Leaf
 */
public interface Node {
    /**
     * Вычисляет стоимость данного элемента.
     *
     * <p>Для конечного элемента ({@link Item}) возвращает его собственную цену.
     * Для контейнера ({@link Box}) — сумму стоимостей всех вложенных элементов.
     *
     * @return стоимость элемента
     */
    Integer calcCost();

    /**
     * Возвращает родительский узел данного элемента.
     *
     * <p>Позволяет перемещаться вверх по дереву, например, чтобы определить,
     * в какой коробке находится товар.
     *
     * @return родительский узел или {@code null}, если элемент является корнем
     */
    Node getParent();

    /**
     * Устанавливает родительский узел данного элемента.
     *
     * @param parent родительский узел
     */
    void setParent(Node parent);
}
