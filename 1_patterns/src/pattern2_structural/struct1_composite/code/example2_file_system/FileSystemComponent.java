package pattern2_structural.struct1_composite.code.example2_file_system;

/**
 * Общий интерфейс элементов файловой системы (роль <b>Component</b> в паттерне Composite).
 *
 * <p>Определяет единый контракт для всех узлов файловой системы — как для
 * файлов ({@link File}), так и для директорий ({@link Directory}).
 * Благодаря этому клиентский код может работать с любым элементом файловой
 * системы единообразно, не зная его конкретный тип.
 *
 * <p><b>Ключевые методы:</b>
 * <ul>
 *   <li>{@link #getSize()} — возвращает размер элемента (для файла — собственный,
 *       для директории — суммарный размер всего содержимого);</li>
 *   <li>{@link #getName()} — возвращает имя элемента;</li>
 *   <li>{@link #showStructure(String)} — выводит древовидную структуру с отступами.</li>
 * </ul>
 *
 * @see FileSystemContainer
 * @see File
 * @see Directory
 */
public interface FileSystemComponent {
    /**
     * Возвращает размер элемента в байтах.
     *
     * <p>Для файла ({@link File}) — его собственный размер.
     * Для директории ({@link Directory}) — сумму размеров всех вложенных элементов.
     *
     * @return размер в байтах
     */
    long getSize();

    /**
     * Возвращает имя элемента файловой системы.
     *
     * @return имя файла или директории
     */
    String getName();

    /**
     * Выводит структуру элемента в консоль с заданным отступом.
     *
     * <p>Для файлов выводит имя и размер. Для директорий — рекурсивно
     * выводит всё содержимое с увеличивающимся отступом, формируя
     * наглядное дерево.
     *
     * @param indent строка отступа для текущего уровня вложенности
     */
    void showStructure(String indent);
}
