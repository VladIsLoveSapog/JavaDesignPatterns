package pattern2_structural.struct6_proxy.code.example2_protection;

/**
 * Паттерн Заместитель (Proxy) — роли пользователей.
 * <p>
 * Перечисление определяет уровни доступа для защищающего прокси.
 * Каждая роль имеет свой набор разрешённых операций:
 * <ul>
 *     <li>{@link #READER} — только чтение</li>
 *     <li>{@link #EDITOR} — чтение и запись</li>
 *     <li>{@link #ADMIN} — чтение, запись и удаление</li>
 * </ul>
 */
public enum Role {

    /** Роль с правами только на чтение документа */
    READER,

    /** Роль с правами на чтение и редактирование документа */
    EDITOR,

    /** Роль с полным доступом: чтение, редактирование и удаление */
    ADMIN
}
