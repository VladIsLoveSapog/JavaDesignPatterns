package pattern2_structural.struct6_proxy.code.example2_protection;

/**
 * Паттерн Заместитель (Proxy) — интерфейс документа (Subject).
 * <p>
 * Определяет общий интерфейс для реального документа и его прокси-заместителя.
 * Клиентский код работает с документом через этот интерфейс, не зная,
 * является ли объект реальным документом или его защищающим прокси.
 * <p>
 * Операции с документом включают чтение, запись и удаление —
 * каждая из которых может требовать разного уровня доступа.
 */
public interface Document {

    /**
     * Читает содержимое документа.
     *
     * @return текстовое содержимое документа
     */
    String read();

    /**
     * Записывает новое содержимое в документ.
     *
     * @param content новое содержимое для записи
     */
    void write(String content);

    /**
     * Удаляет документ.
     */
    void delete();
}
