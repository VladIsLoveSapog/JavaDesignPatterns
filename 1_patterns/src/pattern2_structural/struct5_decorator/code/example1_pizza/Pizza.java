package pattern2_structural.struct5_decorator.code.example1_pizza;

/**
 * Компонент (Component) — базовый интерфейс паттерна Декоратор.
 *
 * <p>Определяет общий контракт для всех пицц — как для конкретных
 * компонентов (например, {@link PepperoniPizza}), так и для декораторов
 * (например, {@link CheesePizzaDecorator}).
 *
 * <p>Благодаря тому, что и компоненты, и декораторы реализуют один
 * и тот же интерфейс, клиентский код может работать с любым объектом
 * единообразно — он не знает, обёрнут объект декоратором или нет.
 */
public interface Pizza {

    /**
     * Возвращает текстовое описание пиццы (состав, ингредиенты).
     *
     * @return описание пиццы
     */
    String getDescription();

    /**
     * Возвращает стоимость пиццы.
     * Каждый декоратор может увеличивать или уменьшать итоговую цену.
     *
     * @return стоимость пиццы
     */
    Double getCost();
}