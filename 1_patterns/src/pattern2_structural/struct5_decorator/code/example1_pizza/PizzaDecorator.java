package pattern2_structural.struct5_decorator.code.example1_pizza;

/**
 * Базовый декоратор (Base Decorator) — абстрактный класс для всех декораторов пиццы.
 *
 * <p>Ключевые обязанности базового декоратора:
 * <ol>
 *   <li><b>Реализовать тот же интерфейс</b>, что и оборачиваемый объект ({@link Pizza}).
 *       Это позволяет подставлять декоратор везде, где ожидается компонент.</li>
 *   <li><b>Хранить ссылку на оборачиваемый объект</b> (поле {@code pizza}).
 *       Через эту ссылку декоратор делегирует вызовы оригинальному компоненту.</li>
 * </ol>
 *
 * <p>Конкретные декораторы (например, {@link CheesePizzaDecorator}) наследуются
 * от этого класса и переопределяют нужные методы, добавляя своё поведение
 * до или после вызова метода обёрнутого объекта.
 */
public abstract class PizzaDecorator implements Pizza {

    /**
     * Ссылка на оборачиваемый объект.
     * protected — чтобы конкретные декораторы имели к нему доступ.
     */
    protected Pizza pizza;

    /**
     * Конструктор принимает объект, который будет обёрнут декоратором.
     *
     * @param pizza оборачиваемый объект (может быть как конкретным компонентом,
     *              так и другим декоратором — это и даёт возможность вложенности)
     */
    public PizzaDecorator(Pizza pizza) {
        this.pizza = pizza;
    }
}
