package pattern2_structural.struct5_decorator.code.example2_message;

/**
 * Компонент (Component) — базовый интерфейс для сообщений.
 *
 * <p>Определяет контракт, которому следуют и конкретные сообщения
 * ({@link SimpleMessage}), и декораторы ({@link MessageDecorator}).
 *
 * <p>Клиентский код работает именно с этим интерфейсом, поэтому
 * ему безразлично, сколько «слоёв» декораторов обернуто вокруг сообщения.
 */
public interface Message {

    /**
     * Возвращает содержимое сообщения.
     * Декораторы могут модифицировать это содержимое (шифровать,
     * добавлять метку времени и т.д.).
     *
     * @return текст сообщения
     */
    String getContent();
}
