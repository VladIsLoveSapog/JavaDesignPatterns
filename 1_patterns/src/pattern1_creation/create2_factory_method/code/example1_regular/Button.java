package pattern1_creation.create2_factory_method.code.example1_regular;

/**
 * Интерфейс продукта — кнопка.
 *
 * Это общий контракт для всех типов кнопок, которые будет создавать фабрика.
 * Конкретные реализации (WindowsButton, MacButton) будут выглядеть по-разному,
 * но клиентский код работает только с этим интерфейсом, не зная о деталях.
 */
public interface Button {

    /**
     * Отрисовать кнопку на экране.
     * Каждая платформа рисует кнопку в своём стиле.
     */
    public void render();

    /**
     * Обработать нажатие кнопки.
     * Логика клика может отличаться на разных платформах.
     */
    public void click();
}
