package pattern1_creation.create2_factory_method.code.example1_regular;

/**
 * Абстрактный создатель — диалоговое окно.
 *
 * Ключевая особенность: этот класс содержит собственную бизнес-логику (метод render),
 * которая использует фабричный метод createButton() внутри себя.
 * При этом Dialog понятия не имеет, какой именно тип кнопки будет создан —
 * это решают подклассы, переопределяя createButton().
 *
 * Такое разделение и есть суть паттерна: «создатель» использует продукт,
 * но не создаёт его напрямую.
 */
public abstract class Dialog {

    /**
     * Фабричный метод — абстрактный метод, который должен переопределить каждый подкласс.
     *
     * Именно этот метод является «фабричным» — он отвечает за создание продукта.
     * Подкласс решает, какой конкретный тип Button создать.
     *
     * @return созданная кнопка (конкретный тип зависит от подкласса)
     */
    public abstract Button createButton();

    /**
     * Метод бизнес-логики, использующий фабричный метод.
     *
     * Обратите внимание: render() вызывает createButton(), но не знает,
     * какой тип кнопки будет возвращён. Это позволяет менять поведение
     * без изменения этого метода.
     */
    public void render() {
        // Вызываем фабричный метод для получения объекта-продукта
        Button button = createButton();

        System.out.println("Диалоговое окно открывается...");

        // Используем продукт через его интерфейс — не знаем конкретный тип
        button.render();

        System.out.println("Диалоговое окно отрисовано. Ожидание нажатия кнопки...");
    }

    /**
     * Ещё один метод бизнес-логики, использующий продукт.
     * Демонстрирует, что создатель может иметь сколько угодно собственных методов.
     */
    public void handleInput() {
        Button button = createButton();
        System.out.println("Пользователь нажал кнопку:");
        button.click();
    }
}
