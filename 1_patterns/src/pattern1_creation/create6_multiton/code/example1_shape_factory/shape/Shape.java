package pattern1_creation.create6_multiton.code.example1_shape_factory.shape;

/**
 * Интерфейс продукта в паттерне Мультитон.
 *
 * <p>Определяет общий контракт для всех геометрических фигур.
 * Каждая конкретная фигура ({@link Circle}, {@link Rectangle}, {@link Triangle})
 * реализует этот интерфейс, предоставляя собственную логику отрисовки.</p>
 *
 * <p>Клиентский код работает именно с этим интерфейсом, не завися
 * от конкретных реализаций фигур.</p>
 */
public interface Shape {

    /**
     * Выполняет отрисовку фигуры.
     *
     * <p>Каждая конкретная реализация выводит информацию о типе
     * фигуры и её имени в стандартный поток вывода.</p>
     */
    void draw();
}
