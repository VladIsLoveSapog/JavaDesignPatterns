package pattern1_creation.create6_multiton.code.example2_app_config;

/**
 * Перечисление окружений приложения — ключ мультитона {@link AppConfigMultiton}.
 *
 * <p>Каждый элемент соответствует ровно одному экземпляру {@link AppConfig}
 * с предзаданными параметрами подключения к базе данных и уровнем логирования.</p>
 *
 * <p>Использование {@code enum} в качестве ключа позволяет применять
 * {@link java.util.EnumMap} для типобезопасного и эффективного хранения
 * конфигураций в мультитоне.</p>
 */
public enum Environment {
    /** Окружение для локальной разработки. */
    DEV,

    /** Окружение для предрелизного тестирования. */
    STAGING,

    /** Продакшн-окружение. */
    PROD
}
