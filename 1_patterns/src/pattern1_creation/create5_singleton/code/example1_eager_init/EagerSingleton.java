package pattern1_creation.create5_singleton.code.example1_eager_init;

/**
 * Eager Initialization Singleton (Жадная инициализация).
 *
 * <p>Экземпляр создаётся в момент загрузки класса JVM, ещё до того,
 * как кто-либо вызовет {@link #getInstance()}.
 *
 * <p><b>Почему это потокобезопасно:</b> JVM гарантирует, что статическая
 * инициализация класса выполняется ровно один раз и завершается до того,
 * как любой поток получит доступ к полям класса (JLS §12.4.2).
 *
 * <p><b>Когда использовать:</b> объект лёгкий и точно понадобится при старте
 * приложения (например, конфигурация, логгер).
 */
public class EagerSingleton {

    /*
     * final — гарантирует, что ссылка не будет перезаписана после инициализации.
     * static — поле принадлежит классу, а не экземпляру, поэтому существует
     *          в единственном экземпляре на весь ClassLoader.
     * private — прямой доступ к полю извне запрещён, только через getInstance().
     */
    private static final EagerSingleton INSTANCE = new EagerSingleton();

    /*
     * Приватный конструктор — запрещает создание экземпляров через new.
     * Без него компилятор сгенерирует публичный конструктор по умолчанию,
     * и любой код сможет создать сколько угодно объектов.
     */
    private EagerSingleton() {}

    /**
     * Единственный способ получить экземпляр синглтона.
     *
     * @return единственный экземпляр {@code EagerSingleton}
     */
    public static EagerSingleton getInstance() {
        return INSTANCE;
    }
}
