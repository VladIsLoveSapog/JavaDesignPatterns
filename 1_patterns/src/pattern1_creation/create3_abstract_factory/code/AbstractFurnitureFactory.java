package pattern1_creation.create3_abstract_factory.code;

/**
 * [AbstractFactory]
 * Интерфейс абстрактной фабрики мебели.
 *
 * <p>Наличие всех трёх методов в одном интерфейсе — это и есть гарантия
 * согласованности семейства: реализация обязана предоставить кресло, диван
 * и стол одного стиля, смешать стили невозможно на уровне типов.
 * В методах используются общие типы ({@link Sofa}, {@link Chair}, {@link Table}),
 * что позволяет достичь слабой связанности — клиент не привязывается к
 * конкретным реализациям классов.
 */
public interface AbstractFurnitureFactory {
    /**
     * Метод создания какого-то дивана
     *
     * @return диван
     */
    Sofa createSofa();

    /**
     * Метод создания какого-то кресла
     *
     * @return кресло
     */
    Chair createChair();

    /**
     * Метод создания какого-то стола
     *
     * @return стол
     */
    Table createTable();
}
