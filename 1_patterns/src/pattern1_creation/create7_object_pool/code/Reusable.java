package pattern1_creation.create7_object_pool.code;

/**
 * Интерфейс повторно используемого объекта для паттерна Object Pool.
 *
 * <p>Любой объект, управляемый пулом, должен реализовать этот интерфейс,
 * чтобы пул мог сбросить его состояние перед повторной выдачей клиенту.
 */
public interface Reusable {

    /**
     * Сбрасывает объект в исходное (чистое) состояние.
     *
     * <p>Вызывается пулом при возврате объекта, чтобы следующий клиент
     * получил объект без «мусора» от предыдущего использования.
     */
    void reset();
}
